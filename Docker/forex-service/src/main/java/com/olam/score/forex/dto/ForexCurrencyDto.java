package com.olam.score.forex.dto;

public class ForexCurrencyDto {
	
	private Integer forexcurrencyId;
	private String forexcurrencyName;
	private String forexcurrencyCode;
	public Integer getForexcurrencyId() {
		return forexcurrencyId;
	}
	public void setForexcurrencyId(Integer forexcurrencyId) {
		this.forexcurrencyId = forexcurrencyId;
	}
	public String getForexcurrencyName() {
		return forexcurrencyName;
	}
	public void setForexcurrencyName(String forexcurrencyName) {
		this.forexcurrencyName = forexcurrencyName;
	}
	public String getForexcurrencyCode() {
		return forexcurrencyCode;
	}
	public void setForexcurrencyCode(String forexcurrencyCode) {
		this.forexcurrencyCode = forexcurrencyCode;
	}
	
	
}
